public class ArithmeticOperator {
    public static void main(String[] args) {

        // 変数宣言 declare variables
        int a = 12;
        int b = 5;

        // 変数内容の表示 print variable status
        System.out.printf("a = %d, b = %d %n%n", a, b);

        // 加算演算子 addition operator
        System.out.printf("a + b = %d %n", a + b);

        // 減算演算子 subtraction operator
        System.out.printf("a - b = %d %n", a - b);

        // 乗算演算子 multiplication operator
        System.out.printf("a * b = %d %n", a * b);

        // 商算演算子 division operator
        System.out.printf("a / b = %d %n", a / b);

        // 余算演算子 modulo operator
        System.out.printf("a %% b = %d %n", a % b);
    }
}
