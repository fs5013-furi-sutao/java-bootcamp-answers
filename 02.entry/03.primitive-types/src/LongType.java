public class LongType {
    public static void main(String[] args) {

        long minLong = Long.MIN_VALUE;
        long maxLong = Long.MAX_VALUE;
        long smallLong = -42332200000L;

        System.out.printf("long 型の最小値 = %s%n", minLong);
        System.out.printf("long 型の最大値 = %s%n", maxLong);
        System.out.println();
        System.out.printf("long 型の負数 -42332200000 = %s%n", smallLong);
    }
}
